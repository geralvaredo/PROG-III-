				$consulta->bindValue(':usuario',$this->usuario, PDO::PARAM_STR);
				$consulta->bindValue(':contrasena', $this->contrasena, PDO::PARAM_STR);
				$consulta->bindValue(':tipo', $this->tipo, PDO::PARAM_STR);
