  	public function __construct($usuario = null,$Contrasena = null,$tipo = null){
  		if($usuario != null){
  		$this->usuario = $usuario;
  		$this->Contrasena = $Contrasena;
  		$this->tipo = $tipo;
  	}

  	}

  	  	public static function TraerTodoLosUsuarios()
	{
			$objetoAccesoDato = AccesoDatos::dameUnObjetoAcceso(); 
			$consulta =$objetoAccesoDato->RetornarConsulta("select id,Usuario as usuario, Contraseña as contrasena,Tipo as tipo from usuarios");
			$consulta->execute();			
			return $consulta->fetchAll(PDO::FETCH_CLASS, "usuario");		
	}

  	public function BorrarUsuario()
	 {
	 		$objetoAccesoDato = AccesoDatos::dameUnObjetoAcceso(); 
			$consulta =$objetoAccesoDato->RetornarConsulta("
				delete 
				from usuarios 				
				WHERE id=:id");	
				$consulta->bindValue(':id',$this->id, PDO::PARAM_INT);		
				$consulta->execute();
				return $consulta->rowCount();
	 }

	public static function BorrarCdPorAnio($año)
